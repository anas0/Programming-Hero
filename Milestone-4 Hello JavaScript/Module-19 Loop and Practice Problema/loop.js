// While Loop
var roastGiven = 0;
while(roastGiven < 7){
    console.log('Roast Den, Please !!!');
    console.log(roastGiven);
    roastGiven++;
}

// For Loop
for(var riceGiven=0; riceGiven<7; riceGiven++){
    console.log('Rice Den, Please !!!');
    console.log(riceGiven);
}

// Array Loop
var numbers = [45, 62, 78, 37, 94, 80];
for(var i=0; i<numbers.length; i++){
    var number = numbers[i];
    console.log(number);
}